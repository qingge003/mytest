/**	
private 即私有的,只有当前类可以调用
static 即静态,定义的属性不用实例化对象,可以通过抽象类直接调用
		不是实例对象类具有的属性,即可以用static修饰
 		静态方法中不可以使用this关键字,不可以调用非静态方法
final 即最后,定义的属性不可被修改,变量在声明时必须赋值,之后不能再更改;
      修饰的类不能被继承;修饰的方法不可被重写,但可被继承
abstract即抽象,修饰的类称为抽象类，不能被实例化,通常被继承
        修饰的方法只需声明，不用实现,即声明一个抽象方法,在其继承的子类中实现

	方法重载：方法名一样，但参数类型，参数顺序，个数不一样,例如构造方法的重载
	         同一个类中可以重载,子类也可以重载父类
	方法重写:子类重写父类方法,并覆盖父类方法
	*/

